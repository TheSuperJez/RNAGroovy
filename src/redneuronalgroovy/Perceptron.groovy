class Perceptron {
    String nombre
    Integer parametro1
    Integer parametro2
    Integer salida1
    Integer salida2
    Integer salida3
    Integer salida4
    public Perceptron(String nombre, Integer parametro1,Integer parametro2, Integer salida1, Integer salida2, Integer salida3, Integer salida4){
        this.nombre=nombre
        this.parametro1=parametro1
        this.parametro2=parametro2
        this.salida1=salida1
        this.salida2=salida2
        this.salida3=salida3
        this.salida4=salida4
    }
}

